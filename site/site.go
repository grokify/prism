// statusVariant maps a capability status to a badge variant.
func statusVariant(status string) string {
	switch status {
	case "operational":
		return "success"
	case "implemented":
		return "primary"
	case "in-progress":
		return "warning"
	case "planned":
		return "default"
	case "deprecated":
		return "error"
	default:
		return "default"
	}
}

// toKebabCase converts a string to kebab-case.
// Examples:
//   - "My Stack" -> "my-stack"
//   - "Operations_SaaS" -> "operations-saas"
//   - "SecurityCapabilities" -> "security-capabilities"
//   - "already-kebab-case" -> "already-kebab-case"
func toKebabCase(s string) string {
	// First, handle camelCase and PascalCase by inserting hyphens before capitals
	var result strings.Builder
	for i, r := range s {
		if unicode.IsUpper(r) {
			if i > 0 {
				prev := rune(s[i-1])
				// Insert hyphen if previous char is lowercase or if we're starting a new word
				if unicode.IsLower(prev) || (i+1 < len(s) && unicode.IsLower(rune(s[i+1]))) {
					result.WriteRune('-')
				}
			}
			result.WriteRune(unicode.ToLower(r))
		} else {
			result.WriteRune(r)
		}
	}

	str := result.String()

	// Replace common separators with hyphens
	str = strings.ReplaceAll(str, "_", "-")
	str = strings.ReplaceAll(str, " ", "-")
	str = strings.ReplaceAll(str, ".", "-")

	// Remove any non-alphanumeric characters except hyphens
	reg := regexp.MustCompile(`[^a-z0-9-]+`)
	str = reg.ReplaceAllString(str, "")

	// Collapse multiple hyphens into one
	reg = regexp.MustCompile(`-+`)
	str = reg.ReplaceAllString(str, "-")

	// Trim leading/trailing hyphens
	str = strings.Trim(str, "-")

	return str
}

