// Package site provides static site generation for PRISM dashboards.
//
// # Standard PRISM Directory Structure
//
// The recommended way to organize PRISM data is using the standard directory structure:
//
//	{stack-name}/
//	├── stack.json           # Capability stack definition (required)
//	├── model.json           # Maturity model - SLIs, levels, criteria (optional)
//	├── state.json           # Current state - SLI values per time window (optional)
//	└── roadmap.json         # OKRs, initiatives, roadmap items (optional)
//
// This structure keeps all related files together and enables automatic discovery.
// The directory name becomes the URL-safe identifier (converted to kebab-case).
//
// # File Descriptions
//
//   - stack.json: Defines capabilities, layers, categories, and their relationships.
//     See github.com/grokify/prism-capability for the schema.
//
//   - model.json: Defines maturity levels, SLIs, and criteria for measuring capability maturity.
//     See github.com/grokify/prism-maturity/maturity for the schema.
//
//   - state.json: Contains current SLI values organized by time windows (7d, 30d, 90d).
//     See github.com/grokify/prism-maturity for the PRISMDocument schema.
//
//   - roadmap.json: Defines OKRs, initiatives, and roadmap items.
//     See github.com/grokify/prism-roadmap for the schema.
package site

