// getSLICurrentLevel calculates the current maturity level for an SLI.
func (g *Generator) getSLICurrentLevel(sd *StackData, sliID string) float64 {
	if sd.State == nil || sd.Model == nil {
		return 1.0
	}

	// Get SLI value from state
	var sliValue float64
	var hasValue bool

	if sd.State.SLIState != nil {
		if state, ok := sd.State.SLIState[sliID]; ok && state != nil && state.Windows != nil {
			if ws, ok := state.Windows["30d"]; ok && ws != nil {
				sliValue = ws.Value
				hasValue = true
			} else {
				for _, ws := range state.Windows {
					if ws != nil {
						sliValue = ws.Value
						hasValue = true
						break
					}
				}
			}
		}
	}

	if !hasValue {
		return 1.0
	}

	// Calculate level based on criteria
	highestLevel := 1.0
	for _, domain := range sd.Model.Domains {
		for _, level := range domain.Levels {
			for _, criterion := range level.Criteria {
				if criterion.SLIID != sliID {
					continue
				}
				if criterion.CheckMet(sliValue) {
					levelNum := float64(level.Level)
					if levelNum > highestLevel {
						highestLevel = levelNum
					}
				}
			}
		}
	}

	return highestLevel
}

// buildThresholdMatrix creates the threshold matrix data for the metrics table.
// This mirrors the XLSX Threshold Matrix sheet structure.
func (g *Generator) buildThresholdMatrix(sd *StackData) []ThresholdMatrixRow {
	if sd.Model == nil {
		return nil
	}

	// Build a map of SLI ID -> level -> threshold string
	type sliInfo struct {
		id         string
		name       string
		category   string
		unit       string
		tags       []string
		frameworks []string
		thresholds map[int]string
	}

	sliMap := make(map[string]*sliInfo)
	var sliOrder []string

	for _, domain := range sd.Model.Domains {
		for _, level := range domain.Levels {
			for _, criterion := range level.Criteria {
				if criterion.SLIID == "" {
					continue
				}

				info, exists := sliMap[criterion.SLIID]
				if !exists {
					sli := sd.Model.GetSLI(criterion.SLIID)
					name := criterion.SLIID
					category := ""
					unit := ""
					var tags []string
					var frameworks []string

					if sli != nil {
						name = sli.Name
						category = sli.Category
						unit = sli.Unit
						tags = sli.GetNormalizedTags()

						// Collect framework names
						fwSet := make(map[string]bool)
						for _, fm := range sli.FrameworkMappings {
							fwSet[fm.Framework] = true
						}
						for fw := range fwSet {
							frameworks = append(frameworks, fw)
						}
						sort.Strings(frameworks)
					}

					info = &sliInfo{
						id:         criterion.SLIID,
						name:       name,
						category:   category,
						unit:       unit,
						tags:       tags,
						frameworks: frameworks,
						thresholds: make(map[int]string),
					}
					sliMap[criterion.SLIID] = info
					sliOrder = append(sliOrder, criterion.SLIID)
				}

				// Format threshold
				threshold := g.formatCriterionThreshold(criterion, info.unit)
				info.thresholds[level.Level] = threshold
			}
		}
	}

	// Sort by category then name
	sort.Slice(sliOrder, func(i, j int) bool {
		a, b := sliMap[sliOrder[i]], sliMap[sliOrder[j]]
		if a.category != b.category {
			return a.category < b.category
		}
		return a.name < b.name
	})

	// Build result rows
	var rows []ThresholdMatrixRow
	for _, sliID := range sliOrder {
		info := sliMap[sliID]

		// Calculate current maturity level
		currentLevel := g.getSLICurrentLevel(sd, sliID)

		// Get current value from state
		currentValue := ""
		if sd.State != nil && sd.State.SLIState != nil {
			if state, ok := sd.State.SLIState[sliID]; ok && state != nil && state.Windows != nil {
				// Try 30d window first, then any available
				var val float64
				var hasVal bool
				if ws, ok := state.Windows["30d"]; ok && ws != nil {
					val = ws.Value
					hasVal = true
				} else {
					for _, ws := range state.Windows {
						if ws != nil {
							val = ws.Value
							hasVal = true
							break
						}
					}
				}
				if hasVal {
					if info.unit != "" {
						currentValue = fmt.Sprintf("%.2f%s", val, info.unit)
					} else {
						currentValue = fmt.Sprintf("%.2f", val)
					}
				}
			}
		}

		row := ThresholdMatrixRow{
			Category:     info.category,
			Name:         info.name,
			Unit:         info.unit,
			Tags:         strings.Join(info.tags, ", "),
			Frameworks:   strings.Join(info.frameworks, ", "),
			CurrentLevel: currentLevel,
			CurrentValue: currentValue,
			TargetQ1:     0, // Can be populated from roadmap data if available
			TargetQ2:     0, // Can be populated from roadmap data if available
			M1:           info.thresholds[1],
			M2:           info.thresholds[2],
			M3:           info.thresholds[3],
			M4:           info.thresholds[4],
			M5:           info.thresholds[5],
		}
		// Fill empty thresholds with "-"
		if row.M1 == "" {
			row.M1 = "-"
		}
		if row.M2 == "" {
			row.M2 = "-"
		}
		if row.M3 == "" {
			row.M3 = "-"
		}
		if row.M4 == "" {
			row.M4 = "-"
		}
		if row.M5 == "" {
			row.M5 = "-"
		}
		rows = append(rows, row)
	}

	return rows
}

// formatCriterionThreshold formats a criterion's threshold for display.
func (g *Generator) formatCriterionThreshold(c maturity.Criterion, unit string) string {
	if c.Operator == "exists" {
		return "Tracked"
	}

	symbol := maturity.OperatorSymbol(c.Operator)
	if unit != "" {
		return fmt.Sprintf("%s%v%s", symbol, c.Target, unit)
	}
	return fmt.Sprintf("%s%v", symbol, c.Target)
}

