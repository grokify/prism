func (g *Generator) generateStackPages() error {
	if err := os.MkdirAll(filepath.Join(g.config.OutputDir, "stacks"), 0755); err != nil {
		return err
	}

	funcMap := template.FuncMap{
		"json": func(v interface{}) template.JS {
			b, _ := json.Marshal(v)
			return template.JS(b) //nolint:gosec // G203: Safe - JSON data for templates
		},
		"statusVariant": statusVariant,
		"kebab":         toKebabCase,
		"maturityLevel": func(capID string, capMaturity map[string]float64) float64 {
			if level, ok := capMaturity[capID]; ok {
				return level
			}
			return 0
		},
		"sliCount": func(capID string, capSLICount map[string]int) int {
			if count, ok := capSLICount[capID]; ok {
				return count
			}
			return 0
		},
	}

	// Parse all templates
	capsByLayerTmpl, err := template.New("caps-by-layer").Funcs(funcMap).Parse(capsByLayerTemplate)
	if err != nil {
		return fmt.Errorf("parsing caps-by-layer template: %w", err)
	}
	capsByCategoryTmpl, err := template.New("caps-by-category").Funcs(funcMap).Parse(capsByCategoryTemplate)
	if err != nil {
		return fmt.Errorf("parsing caps-by-category template: %w", err)
	}
	metricsByLayerTmpl, err := template.New("metrics-by-layer").Funcs(funcMap).Parse(metricsByLayerTemplate)
	if err != nil {
		return fmt.Errorf("parsing metrics-by-layer template: %w", err)
	}
	metricsByCategoryTmpl, err := template.New("metrics-by-category").Funcs(funcMap).Parse(metricsByCategoryTemplate)
	if err != nil {
		return fmt.Errorf("parsing metrics-by-category template: %w", err)
	}
	metricsTableTmpl, err := template.New("metrics-table").Funcs(funcMap).Parse(metricsTableTemplate)
	if err != nil {
		return fmt.Errorf("parsing metrics-table template: %w", err)
	}

	for _, sd := range g.stacks {
		// Build capability maturity and SLI count maps
		capMaturity := make(map[string]float64)
		capSLICount := make(map[string]int)
		if sd.Maturity != nil {
			for _, cm := range sd.Maturity.GetCapabilityMaturities() {
				capMaturity[cm.CapabilityID] = cm.AggregateLevel
				capSLICount[cm.CapabilityID] = len(cm.SLIIDs)
			}
		}

		// Build grouped data
		capsByLayer := g.buildCapabilitiesByLayer(sd)
		capsByCategory := g.buildCapabilitiesByCategory(sd)
		metricsByLayer := g.buildSLIsByLayer(sd)
		metricsByCat := g.buildSLIsByCategory(sd)
		thresholdMatrix := g.buildThresholdMatrix(sd)

		// Convert threshold matrix to JSON for template
		var thresholdMatrixJSON template.JS
		if jsonData, err := json.Marshal(thresholdMatrix); err == nil {
			thresholdMatrixJSON = template.JS(jsonData) //nolint:gosec // G203: Safe - JSON data for templates
		} else {
			thresholdMatrixJSON = "[]"
		}

		baseData := StackPageData{
			Title:               sd.Stack.Metadata.Title,
			Theme:               g.config.Theme,
			Stack:               sd.Stack,
			StackData:           sd,
			CapMaturity:         capMaturity,
			CapSLICount:         capSLICount,
			CapsByLayer:         capsByLayer,
			CapsByCategory:      capsByCategory,
			MetricsByLayer:      metricsByLayer,
			MetricsByCat:        metricsByCat,
			ThresholdMatrix:     thresholdMatrix,
			ThresholdMatrixJSON: thresholdMatrixJSON,
			SiteTitle:           g.config.Title,
			BaseURL:             g.config.BaseURL,
			AllStacks:           g.stacks,
			HasSiteNavJS:        g.config.SiteNavJS != "",
			HasModel:            sd.Model != nil,
			HideGeneratedBy:     g.config.HideGeneratedBy,
		}

		// Generate 5 pages per stack (alphabetically ordered)
		pages := []struct {
			filename    string
			pageTitle   string
			currentPage string
			tmpl        *template.Template
		}{
			{"capabilities-by-category-" + sd.BaseName + ".html", "Capabilities by Category", "caps-by-category", capsByCategoryTmpl},
			{"capabilities-by-layer-" + sd.BaseName + ".html", "Capabilities by Layer", "caps-by-layer", capsByLayerTmpl},
			{"metrics-" + sd.BaseName + ".html", "Metrics", "metrics", metricsTableTmpl},
			{"metrics-by-category-" + sd.BaseName + ".html", "Metrics by Category", "metrics-by-category", metricsByCategoryTmpl},
			{"metrics-by-layer-" + sd.BaseName + ".html", "Metrics by Layer", "metrics-by-layer", metricsByLayerTmpl},
		}

		for _, page := range pages {
			data := baseData
			data.PageTitle = page.pageTitle
			data.CurrentPage = page.currentPage

			outPath := filepath.Join(g.config.OutputDir, "stacks", page.filename)
			f, err := os.Create(outPath)
			if err != nil {
				return err
			}

			if err := page.tmpl.Execute(f, data); err != nil {
				f.Close()
				return err
			}
			f.Close()
		}
	}

	return nil
}

// buildCapabilitiesByLayer groups capabilities by their LayerID field.
func (g *Generator) buildCapabilitiesByLayer(sd *StackData) []CapabilityGroup {
	layerMap := make(map[string][]capstack.Capability)
	layerOrder := make(map[string]int)
	layerNames := make(map[string]string)

	// Get layer order and names from stack
	for i, layer := range sd.Stack.Layers {
		layerOrder[layer.ID] = i
		layerNames[layer.ID] = layer.Name
	}

	// Group capabilities by their LayerID
	for _, cap := range sd.Stack.AllCapabilities() {
		layerID := cap.LayerID
		if layerID == "" {
			layerID = "other"
		}
		layerMap[layerID] = append(layerMap[layerID], cap)
		if _, ok := layerOrder[layerID]; !ok {
			layerOrder[layerID] = 100
			layerNames[layerID] = layerID
		}
	}

	// Build sorted groups
	var groups []CapabilityGroup
	for layerID, caps := range layerMap {
		groups = append(groups, CapabilityGroup{
			ID:           layerID,
			Name:         layerNames[layerID],
			Order:        layerOrder[layerID],
			Capabilities: caps,
		})
	}

	sort.Slice(groups, func(i, j int) bool {
		return groups[i].Order < groups[j].Order
	})

	return groups
}

func (g *Generator) generateCapabilityPages() error {
	if err := os.MkdirAll(filepath.Join(g.config.OutputDir, "capabilities"), 0755); err != nil {
		return err
	}

	tmpl, err := template.New("capability").Funcs(template.FuncMap{
		"json": func(v interface{}) template.JS {
			b, _ := json.Marshal(v)
			return template.JS(b) //nolint:gosec // G203: Safe - JSON data for templates
		},
		"kebab": toKebabCase,
	}).Parse(capabilityPageTemplate)
	if err != nil {
		return err
	}

	for _, sd := range g.stacks {
		for _, cap := range sd.Stack.AllCapabilities() {
			// Get maturity info if available
			var maturityLevel float64
			var sliCount int
			var sliBullets []SLIBulletData

			if sd.Maturity != nil {
				for _, cm := range sd.Maturity.GetCapabilityMaturities() {
					if cm.CapabilityID == cap.ID {
						maturityLevel = cm.AggregateLevel
						sliCount = len(cm.SLIIDs)
						// Build bullet data for each SLI
						sliBullets = g.buildSLIBullets(sd, cm.SLIIDs, cm.SLILevels)
						break
					}
				}
			}

			data := struct {
				Title           string
				Theme           string
				Capability      capstack.Capability
				Stack           *capstack.CapabilityStack
				StackData       *StackData
				MaturityLevel   float64
				SLICount        int
				SLIBullets      []SLIBulletData
				SiteTitle       string
				BaseURL         string
				HasSiteNavJS    bool
				AllStacks       []*StackData
				HideGeneratedBy bool
			}{
				Title:           cap.Name,
				Theme:           g.config.Theme,
				Capability:      cap,
				Stack:           sd.Stack,
				StackData:       sd,
				MaturityLevel:   maturityLevel,
				SLICount:        sliCount,
				SLIBullets:      sliBullets,
				SiteTitle:       g.config.Title,
				BaseURL:         g.config.BaseURL,
				HasSiteNavJS:    g.config.SiteNavJS != "",
				AllStacks:       g.stacks,
				HideGeneratedBy: g.config.HideGeneratedBy,
			}

			// Use stack-capability as filename to avoid conflicts
			capID := toKebabCase(cap.ID)
			outPath := filepath.Join(g.config.OutputDir, "capabilities", sd.BaseName+"-"+capID+".html")
			f, err := os.Create(outPath)
			if err != nil {
				return err
			}

			if err := tmpl.Execute(f, data); err != nil {
				f.Close()
				return err
			}
			f.Close()
		}
	}

	return nil
}

func (g *Generator) copyAssets() error {
	assetsDir := filepath.Join(g.config.OutputDir, "assets")
	if err := os.MkdirAll(assetsDir, 0755); err != nil {
		return err
	}

	// Write embedded CSS
	cssPath := filepath.Join(assetsDir, "site.css")
	if err := os.WriteFile(cssPath, []byte(siteCSS), 0644); err != nil { //nolint:gosec // G306: Web assets need 0644 for server access
		return err
	}

	// If custom site-nav JS provided, copy it
	if g.config.SiteNavJS != "" {
		jsData, err := os.ReadFile(g.config.SiteNavJS)
		if err != nil {
			return fmt.Errorf("reading site-nav JS: %w", err)
		}
		jsPath := filepath.Join(assetsDir, "site-nav.es.js")
		if err := os.WriteFile(jsPath, jsData, 0644); err != nil { //nolint:gosec // G306: Web assets need 0644 for server access
			return err
		}
	}

	return nil
}

// buildSLIBullets creates bullet chart data for a list of SLIs.
func (g *Generator) buildSLIBullets(sd *StackData, sliIDs []string, sliLevels []float64) []SLIBulletData {
	var bullets []SLIBulletData

	if sd.Model == nil {
		return bullets
	}

	for i, sliID := range sliIDs {
		// Get SLI definition
		sli := sd.Model.GetSLI(sliID)
		if sli == nil {
			continue
		}

		// Get current level
		currentLevel := 1.0
		if i < len(sliLevels) {
			currentLevel = sliLevels[i]
		}

		// Get current value from state
		var currentValue float64
		if sd.State != nil && sd.State.SLIState != nil {
			if state, ok := sd.State.SLIState[sliID]; ok && state != nil && state.Windows != nil {
				// Try 30d window first
				if ws, ok := state.Windows["30d"]; ok && ws != nil {
					currentValue = ws.Value
				} else {
					for _, ws := range state.Windows {
						if ws != nil {
							currentValue = ws.Value
							break
						}
					}
				}
			}
		}

		// Build thresholds from criteria
		thresholds := g.buildSLIThresholds(sd.Model, sliID)

		bullet := SLIBulletData{
			ID:           sliID,
			Title:        sli.Name,
			Subtitle:     sli.MetricName,
			Measures:     []float64{currentLevel},
			Markers:      []float64{},        // Target could be added if available
			Ranges:       []float64{3, 4, 5}, // M1-M3, M4, M5 zones
			Thresholds:   thresholds,
			CurrentValue: currentValue,
			Unit:         sli.Unit,
		}

		bullets = append(bullets, bullet)
	}

	return bullets
}

// buildSLIThresholds extracts M1-M5 threshold values for an SLI.
func (g *Generator) buildSLIThresholds(spec *maturity.Spec, sliID string) []SLIThreshold {
	var thresholds []SLIThreshold

	for _, domain := range spec.Domains {
		for _, level := range domain.Levels {
			for _, criterion := range level.Criteria {
				if criterion.SLIID != sliID {
					continue
				}

				// Format value string
				var valueStr string
				if criterion.IsQualitative() {
					valueStr = "Tracked"
				} else {
					unit := criterion.GetUnit(spec)
					if unit != "" {
						valueStr = fmt.Sprintf("%.0f%s", criterion.Target, unit)
					} else {
						valueStr = fmt.Sprintf("%.0f", criterion.Target)
					}
				}

				thresholds = append(thresholds, SLIThreshold{
					Level:    level.Level,
					Value:    criterion.Target,
					ValueStr: valueStr,
					Operator: criterion.Operator,
				})
			}
		}
	}

	return thresholds
}

// buildCapabilitiesByCategory groups capabilities by their CategoryID field.
// This mirrors how buildCapabilitiesByLayer works, using the stack's Categories
// for ordering and naming instead of deriving category from SLIs.
func (g *Generator) buildCapabilitiesByCategory(sd *StackData) []CapabilityGroup {
	categoryMap := make(map[string][]capstack.Capability)
	categoryOrder := make(map[string]int)
	categoryNames := make(map[string]string)

	// Get category order and names from stack
	for i, cat := range sd.Stack.Categories {
		categoryOrder[cat.ID] = i
		categoryNames[cat.ID] = cat.Name
	}

	// Group capabilities by their CategoryID
	for _, cap := range sd.Stack.AllCapabilities() {
		categoryID := cap.CategoryID
		if categoryID == "" {
			categoryID = "uncategorized"
		}
		categoryMap[categoryID] = append(categoryMap[categoryID], cap)
		if _, ok := categoryOrder[categoryID]; !ok {
			categoryOrder[categoryID] = 100 // default order for unknown categories
			// Capitalize first letter for unknown categories
			name := categoryID
			if len(name) > 0 {
				name = strings.ToUpper(name[:1]) + name[1:]
			}
			categoryNames[categoryID] = name
		}
	}

	// Build sorted groups
	var groups []CapabilityGroup
	for catID, caps := range categoryMap {
		name := categoryNames[catID]
		if name == "" {
			name = catID
		}
		groups = append(groups, CapabilityGroup{
			ID:           catID,
			Name:         name,
			Order:        categoryOrder[catID],
			Capabilities: caps,
		})
	}

	// Sort by order
	sort.Slice(groups, func(i, j int) bool {
		return groups[i].Order < groups[j].Order
	})

	return groups
}

// buildSLIsByLayer groups all SLIs with bullet data by layer.
func (g *Generator) buildSLIsByLayer(sd *StackData) []SLIGroup {
	if sd.Model == nil {
		return nil
	}

	layerMap := make(map[string][]SLIBulletData)
	layerOrder := make(map[string]int)
	layerNames := make(map[string]string)

	// Get layer order from stack
	for i, layer := range sd.Stack.Layers {
		layerOrder[layer.ID] = i
		layerNames[layer.ID] = layer.Name
	}

	// Build SLI bullets for all SLIs, grouped by layer
	for _, sli := range sd.Model.AllSLIs() {
		layerID := sli.Layer
		if layerID == "" {
			layerID = "other"
		}

		// Get current level for this SLI
		var currentLevel float64 = 1.0
		if sd.Maturity != nil {
			// Find SLI level
			currentLevel = g.getSLICurrentLevel(sd, sli.ID)
		}

		// Get current value
		var currentValue float64
		if sd.State != nil && sd.State.SLIState != nil {
			if state, ok := sd.State.SLIState[sli.ID]; ok && state != nil && state.Windows != nil {
				if ws, ok := state.Windows["30d"]; ok && ws != nil {
					currentValue = ws.Value
				}
			}
		}

		bullet := SLIBulletData{
			ID:           sli.ID,
			Title:        sli.Name,
			Subtitle:     sli.MetricName,
			Measures:     []float64{currentLevel},
			Markers:      []float64{},
			Ranges:       []float64{3, 4, 5},
			Thresholds:   g.buildSLIThresholds(sd.Model, sli.ID),
			CurrentValue: currentValue,
			Unit:         sli.Unit,
		}

		layerMap[layerID] = append(layerMap[layerID], bullet)
		if _, ok := layerOrder[layerID]; !ok {
			layerOrder[layerID] = 100
			layerNames[layerID] = strings.ToUpper(layerID[:1]) + layerID[1:]
		}
	}

	// Build sorted groups
	var groups []SLIGroup
	for layerID, slis := range layerMap {
		groups = append(groups, SLIGroup{
			ID:    layerID,
			Name:  layerNames[layerID],
			Order: layerOrder[layerID],
			SLIs:  slis,
		})
	}

	sort.Slice(groups, func(i, j int) bool {
		return groups[i].Order < groups[j].Order
	})

	return groups
}

