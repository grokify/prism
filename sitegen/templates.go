// capabilityFilterScript is the JavaScript for filtering capabilities.
const capabilityFilterScript = `
  <script>
    // Filter logic for capabilities
    function applyCapabilityFilters() {
      const activeStatuses = new Set();
      const activeMaturities = new Set();

      document.querySelectorAll('#status-filters .filter-btn.active').forEach(btn => {
        activeStatuses.add(btn.dataset.value);
      });
      document.querySelectorAll('#maturity-filters .filter-btn.active').forEach(btn => {
        activeMaturities.add(btn.dataset.value);
      });

      document.querySelectorAll('.capability-card').forEach(card => {
        const status = card.dataset.status || '';
        const maturity = card.dataset.maturity || '0';

        const statusMatch = activeStatuses.size === 0 || activeStatuses.has(status);
        const maturityMatch = activeMaturities.size === 0 || maturity === '0' || activeMaturities.has(maturity);

        if (statusMatch && maturityMatch) {
          card.classList.remove('filtered-out');
        } else {
          card.classList.add('filtered-out');
        }
      });
    }

    // Toggle filter buttons
    document.querySelectorAll('.filter-btn').forEach(btn => {
      btn.addEventListener('click', () => {
        btn.classList.toggle('active');
        applyCapabilityFilters();
      });
    });

    // Initial filter application
    applyCapabilityFilters();
  </script>
`

