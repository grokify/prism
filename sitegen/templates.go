// metricsFilterScript is the JavaScript for filtering metrics.
const metricsFilterScript = `
  <script>
    // Filter logic for metrics - hides filtered rows completely for vertical collapse
    function applyMetricsFilters() {
      const activeMaturities = new Set();

      document.querySelectorAll('#maturity-filters .filter-btn.active').forEach(btn => {
        activeMaturities.add(parseInt(btn.dataset.value));
      });

      let totalVisible = 0;

      // Filter individual metric rows
      document.querySelectorAll('.bullet-row').forEach(row => {
        const levelBadge = row.querySelector('.sli-level-badge');
        let level = 0;

        if (levelBadge) {
          // Extract level from badge class (e.g., "level-m3" -> 3)
          const levelMatch = levelBadge.className.match(/level-m(\d)/);
          level = levelMatch ? parseInt(levelMatch[1]) : 0;
        }

        // Show row if no filters active OR level matches active filters
        // Also show rows with level 0 (no level assigned) when any filter is active
        const shouldShow = activeMaturities.size === 0 ||
                          activeMaturities.size === 5 ||
                          activeMaturities.has(level) ||
                          (level === 0 && activeMaturities.size > 0);

        if (shouldShow) {
          row.style.display = '';
          totalVisible++;
        } else {
          row.style.display = 'none';
        }
      });

      // Hide empty groups (categories/layers with no visible metrics)
      document.querySelectorAll('.metrics-group').forEach(group => {
        const container = group.querySelector('.bullet-container');
        if (!container) return;

        const visibleRows = container.querySelectorAll('.bullet-row:not([style*="display: none"])');
        const countBadge = group.querySelector('.metrics-group-count');

        if (visibleRows.length === 0) {
          group.style.display = 'none';
        } else {
          group.style.display = '';
          // Update count in group header
          if (countBadge) {
            countBadge.textContent = visibleRows.length + ' metrics';
          }
        }
      });
    }

    // Toggle filter buttons
    document.querySelectorAll('.filter-btn').forEach(btn => {
      btn.addEventListener('click', () => {
        btn.classList.toggle('active');
        applyMetricsFilters();
      });
    });

    // Apply filters after bullet charts are rendered
    const origRenderBulletCharts = renderBulletCharts;
    renderBulletCharts = function() {
      origRenderBulletCharts();
      setTimeout(applyMetricsFilters, 100);
    };
  </script>
`

